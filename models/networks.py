""" The basic structure of network
Contains the network of generator, discriminator, loss function, etc.
Based on  https://github.com/tensorflow/models/blob/master/transformer/spatial_transformer.py.
"""


class Identity(nn.Module):
    def forward(self, x):
        return x


def get_scheduler(optimizer, opt):
    """Get the scheduler for learning rate update"""
    def lambda_rule(epoch):
        lr_l = 1.0 - max(0, epoch + opt.epoch_count -
                         opt.n_epochs) / float(opt.n_epochs_decay + 1)
        return lr_l
    scheduler = lr_scheduler.LambdaLR(optimizer, lr_lambda=lambda_rule)

    return scheduler


def init_weights(net, init_type='normal', init_gain=0.02):
    """Initialize the weight"""
    def init_func(m):
        classname = m.__class__.__name__
        if hasattr(m, 'weight') and (classname.find('Conv') != -1 or classname.find('Linear') != -1):
            init.normal_(m.weight.data, 0.0, init_gain)
            if hasattr(m, 'bias') and m.bias is not None:
                init.constant_(m.bias.data, 0.0)
        elif classname.find('BatchNorm2d') != -1:
            init.normal_(m.weight.data, 1.0, init_gain)
            init.constant_(m.bias.data, 0.0)

    print('initialize network with %s' % init_type)
    net.apply(init_func)


def init_net(net, init_type='normal', init_gain=0.02, gpu_ids=[]):
    """Initialize the net"""
    if len(gpu_ids) > 0:
        assert(torch.cuda.is_available())
        net.to(gpu_ids[0])
        net = torch.nn.DataParallel(net, gpu_ids)
    init_weights(net, init_type, init_gain=init_gain)
    return net


def define_G(input_nc, output_nc, ngf, netG, use_dropout=False, gpu_ids=[],  init_type='normal', init_gain=0.02):
    """Define the generator network"""
    net = None
    norm_layer = functools.partial(
        nn.BatchNorm2d, affine=True, track_running_stats=True)

    if netG == 'resnet_9blocks':
        net = ResnetGenerator(
            input_nc, output_nc, ngf, norm_layer=norm_layer, use_dropout=use_dropout, n_blocks=9)
    elif netG == 'resnet_6blocks':
        net = ResnetGenerator(
            input_nc, output_nc, ngf, norm_layer=norm_layer, use_dropout=use_dropout, n_blocks=6)
    elif netG == 'unet_128':
        net = UnetGenerator(input_nc, output_nc, 7, ngf,
                            norm_layer=norm_layer, use_dropout=use_dropout)
    elif netG == 'unet_256':
        net = UnetGenerator(input_nc, output_nc, 8, ngf,
                            norm_layer=norm_layer, use_dropout=use_dropout)
    else:
        raise NotImplementedError(
            'Generator model name [%s] is not recognized' % netG)
    return init_net(net, init_type, init_gain, gpu_ids)


def define_D(input_nc, ndf,  n_layers_D=3, gpu_ids=[], init_type='normal', init_gain=0.02):
    """Define the discriminator network"""
    net = None
    norm_layer = functools.partial(
        nn.BatchNorm2d, affine=True, track_running_stats=True)
    net = NLayerDiscriminator(input_nc, ndf, n_layers=3, norm_layer=norm_layer)
